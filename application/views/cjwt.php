        <p class="h1">常见问题</p>
        <p class="h2">发布和快速发布有什么不同？</p>
        <p class="h3">发布---客户需要在登录零工宝状态下，按照要求规范发布信息；</p>
        <p class="h3">快速发布---客户未注册为会员时，可以使用这个功能，免费体验，发布信息，但是一个联系手机号码只能快速发布一次。完成后，网站会自动生成一个会员账号，客户应及时登录零工宝完善信息资料。</p>
        <p class="h2">发布信息的数量有什么限制吗？</p>
        <p class="h3">目前零工在线对发布的信息数量没有限制。普通会员发布信息每条2个零工币，购买某种套餐的VIP会员发布信息是免费的。</p>
        <p class="h2">发布信息有什么规则？</p>
        <p class="h3">a、注册会员可以到零工宝（用户中心）界面，点击发布按钮按照提示操作。未注册会员的用户可以使用手机版界面，快速发布功能，快速发布是免费的，信息不分类，快速发布的信息展示在最新发布里面。</p>
        <p class="h3">b、客户发布的信息，凡是上传的图片及填写的文字性等内容都需要人工审核，含有敏感违禁词汇的信息无法成功发布。审核依据司法机关和网监部门等监管机构的法律法规，请不要发布违法信息，否则信息将无法展示或者会被删除。</p>
        <p class="h3">c、不允许重复发布信息，若有需要，客户可以使用“刷新”来代替发布。</p>
        <p class="h2">如何编辑已经发布的信息？</p>
        <p class="h3">客户可以到零工宝（用户中心）对已经发布的信息进行修改、刷新、删除等操作。</p>
        <p class="h2">如何管理我的账户？</p>
        <p class="h3">零工币是客户在零工在线平台使用服务时的支付形式，客户需要充值购买零工币，也可以通过参与零工在线的相应活动来获得零工币。1个零工币=1元人民币。客户需要到零工宝（用户中心）界面管理自己的账户，进行充值或消费，请严谨保密自己的会员名及密码。</p>
        <p class="h2">用户如何对零工进行评价？</p>
        <p class="h3">评价是用户对零工提供的服务质量的意见表达，用户必须严格遵循实事求是的原则给予评价。用户可以通过某零工的详情页或网站首页上的评价按钮，进入评价页面操作。零工在线要求用户必须注册为会员后才能评价，同一用户一年内只允许评价同一位零工一次。如果用户有特殊要求，可以联系零工在线客服解决问题400-860-6286。</p>
